import math

print(round(1.3))  # Redondea al entero más cercano
print(round(1.5))
print(round(1.6))
print(abs(-77))  # Valor absoluto
print(abs(55))

print(math.ceil(1.3))  # Redondea al entero más cercano hacia arriba
print(math.floor(1.999))  # Redondea al entero más cercano hacia abajo
print(math.isnan(23))  # Devuelve True si el valor es NaN (Not a Number)
# print(math.isnan("23")) Solo funciona con números y no con strings
print(math.pow(2, 3))  # Potencia
print(math.sqrt(9))  # Raíz cuadrada
print(math.factorial(5))  # Factorial es el producto de todos los números enteros positivos desde 1 hasta n
# siendo n un número natural
# Es decir, el factorial de 5 es 5*4*3*2*1 = 120
print(math.fabs(-5))  # Valor absoluto que es igual a abs()
